class FDAM_unit(nn.Module):
    def __init__(self, channels, residual=True, use_conv=True, use_selfatt=True):
        super().__init__()
        self.use_conv = use_conv
        self.use_selfatt = use_selfatt
        if use_conv:
            self.res_block = ResnetBlock(in_channels=channels,
                                   out_channels=channels,
                                   temb_channels=0,
                                   dropout=0.0)
            self.res_block_1 = Res2Net(Bottle2neck,
                                     [1, 1, 1, 1],
                                     baseWidth=26,
                                     scale=4)
            self.res_block_2 = Res2Net(Bottle2neck,
                                     [1, 1, 1, 1],
                                     baseWidth=26,
                                     scale=4)
            self.res_block_3 = Res2Net(Bottle2neck,
                                       [1, 1, 1, 1],
                                       baseWidth=26,
                                       scale=4)
        if use_selfatt:        
            self.self_attn_block1 = AttnBlock(channels, residual=residual)
            self.self_attn_block2 = AttnBlock(channels, residual=residual)
            self.self_attn_block3 = AttnBlock(channels, residual=residual)
        self.attn_block1 = AttnBlock(channels, residual=residual)
        self.attn_block2 = AttnBlock(channels, residual=residual)

    def forward(self, x, ref):
        if self.use_conv:
            x = self.res_block(x)
        if self.use_selfatt:
            x = self.self_attn_block1(x)
        if ref is not None:
            x = self.attn_block1(x, ref)
            ref_1 = ref
            ref_s = self.self_attn_block2(ref)
            ref_conv = self.res_block_1(ref)
            ref = ref_s + ref_conv + ref_1
            x_temp = self.res_block_2(x)
            x = x + x_temp
            x = self.attn_block2(x, ref)

        return x
    
class FDAM(nn.Module):
    def __init__(self, channels, block_num=6, residual=True, use_conv=True, use_selfatt=True):
        super().__init__()
        blocks = []
        for i in range(block_num):
            blocks.append(FDAM_unit(channels, residual=residual, use_conv=use_conv, use_selfatt=use_selfatt))
        self.blocks = torch.nn.Sequential(*blocks)

    def forward(self, x, ref, return_all=False):
        x_list = []
        for block in self.blocks:
            x = block(x, ref)
            x_list.append(x)
        if return_all:
            return x_list
        return x
    
    
    
    

